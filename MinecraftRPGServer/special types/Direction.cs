public enum Direction : byte
{
    /// <summary>
    /// -Y
    /// </summary>
    Bottom = 0,
    /// <summary>
    /// +Y
    /// </summary>
    Top,
    /// <summary>
    /// -Z
    /// </summary>
    North,
    /// <summary>
    /// +Z
    /// </summary>
    South,
    /// <summary>
    /// -X
    /// </summary>
    West,
    /// <summary>
    /// +X
    /// </summary>
    East
}